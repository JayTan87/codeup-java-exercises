public class StringExercise {
    public static void main(String[] args) {
        String messageOne = "We don't need no education";
        String messageTwo = "We don't need no thought control";
        String messageThree = "Check \"this\" out!, \"s inside of \"s!";
        String messageFour = "In windows, the main drive is usually C:\\";
        String messageFive = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(messageOne);
        System.out.println(messageTwo);
        System.out.println(messageThree);
        System.out.println(messageFour);
        System.out.println(messageFive);
    }

}

